#!/usr/bin/env node
/**
 * Pull New Bricks design tokens from Figma REST API into /design-system.
 * Requires FIGMA_ACCESS_TOKEN (read access to the file).
 *
 * @see design-system/README.md
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");
const DS = path.join(ROOT, "design-system");
const META_PATH = path.join(DS, "new-bricks.meta.json");
const TOKENS_OUT = path.join(DS, "new-bricks.tokens.json");
const CSS_OUT = path.join(DS, "new-bricks.generated.css");

const TOKEN = process.env.FIGMA_ACCESS_TOKEN;

function loadMeta() {
  const raw = fs.readFileSync(META_PATH, "utf8");
  return JSON.parse(raw);
}

async function figmaGet(url, headers) {
  const res = await fetch(url, { headers });
  const text = await res.text();
  let json;
  try {
    json = JSON.parse(text);
  } catch {
    throw new Error(`Figma API non-JSON (${res.status}): ${text.slice(0, 200)}`);
  }
  if (!res.ok) {
    throw new Error(`Figma API ${res.status}: ${json.err || json.message || text.slice(0, 200)}`);
  }
  return json;
}

function rgba01ToHex({ r, g, b, a = 1 }) {
  const R = Math.round(Math.min(1, Math.max(0, r)) * 255);
  const G = Math.round(Math.min(1, Math.max(0, g)) * 255);
  const B = Math.round(Math.min(1, Math.max(0, b)) * 255);
  const hex = (n) => n.toString(16).padStart(2, "0");
  if (a >= 1) return `#${hex(R)}${hex(G)}${hex(B)}`;
  const A = Math.round(Math.min(1, Math.max(0, a)) * 255);
  return `#${hex(R)}${hex(G)}${hex(B)}${hex(A)}`;
}

function cssVarNameFromVariableName(name) {
  return (
    "--nb-" +
    String(name)
      .trim()
      .toLowerCase()
      .replace(/\s+/g, "-")
      .replace(/[^a-z0-9\-_/]/g, "")
      .replace(/\//g, "-")
  );
}

function pickDefaultModeId(collection) {
  const modes = collection?.modes;
  if (!modes || !modes.length) return null;
  return modes[0]?.modeId ?? null;
}

function flattenVariables(meta) {
  const collections = meta?.variableCollections ?? {};
  const variables = meta?.variables ?? {};
  const out = {};
  const byCollection = {};

  for (const [cid, col] of Object.entries(collections)) {
    const modeId = pickDefaultModeId(col);
    byCollection[col.name || cid] = { id: cid, defaultModeId: modeId, variableIds: col.variableIds ?? [] };
  }

  for (const [vid, v] of Object.entries(variables)) {
    const colId = v.variableCollectionId;
    const col = collections[colId];
    const modeId = pickDefaultModeId(col);
    const rawVal = modeId && v.valuesByMode ? v.valuesByMode[modeId] : undefined;
    let resolved = rawVal;

    if (v.resolvedType === "COLOR" && rawVal && typeof rawVal === "object" && "r" in rawVal) {
      resolved = rgba01ToHex(rawVal);
    }
    if (v.resolvedType === "FLOAT" && typeof rawVal === "number") {
      resolved = rawVal;
    }
    if (v.resolvedType === "STRING" && typeof rawVal === "string") {
      resolved = rawVal;
    }

    out[v.name || vid] = {
      id: vid,
      type: v.resolvedType,
      collectionId: colId,
      collectionName: col?.name,
      defaultModeId: modeId,
      value: resolved ?? rawVal ?? null,
    };
  }

  return { collections: byCollection, tokens: out, rawVariableCount: Object.keys(variables).length };
}

function buildCssRoot(flat) {
  const lines = ["/** Generated by scripts/figma-sync-new-bricks.mjs — do not edit by hand */", ":root {"];
  for (const [name, row] of Object.entries(flat.tokens)) {
    if (row.type !== "COLOR" || typeof row.value !== "string" || !row.value.startsWith("#")) continue;
    const key = cssVarNameFromVariableName(name);
    lines.push(`  ${key}: ${row.value};`);
  }
  lines.push("}");
  return lines.join("\n") + "\n";
}

async function main() {
  if (!TOKEN) {
    console.error("Missing FIGMA_ACCESS_TOKEN. See design-system/README.md");
    process.exit(1);
  }

  const meta = loadMeta();
  const fileKey = meta.figmaFileKey;
  if (!fileKey) {
    console.error("new-bricks.meta.json missing figmaFileKey");
    process.exit(1);
  }

  const headers = { "X-Figma-Token": TOKEN };

  const varsUrl = `https://api.figma.com/v1/files/${fileKey}/variables/local`;
  const fileUrl = `https://api.figma.com/v1/files/${fileKey}?depth=1`;

  console.log("Fetching variables/local …");
  const varsJson = await figmaGet(varsUrl, headers);

  if (varsJson.status !== 200 && varsJson.error) {
    throw new Error(`Variables endpoint: ${JSON.stringify(varsJson)}`);
  }

  const metaBlock = varsJson.meta || {};
  const flat = flattenVariables(metaBlock);

  console.log("Fetching file (depth=1) for style index …");
  const fileJson = await figmaGet(fileUrl, headers);

  const styles = {
    paint: [],
    text: [],
    effect: [],
    grid: [],
  };
  if (fileJson.styles && typeof fileJson.styles === "object") {
    for (const [sid, s] of Object.entries(fileJson.styles)) {
      const row = { id: sid, name: s.name, styleType: s.styleType };
      const t = (s.styleType || "").toLowerCase();
      if (t === "fill" || t === "paint") styles.paint.push(row);
      else if (t === "text") styles.text.push(row);
      else if (t === "effect") styles.effect.push(row);
      else if (t === "grid") styles.grid.push(row);
      else styles.paint.push(row);
    }
  }

  const now = new Date().toISOString();
  const payload = {
    meta: {
      designSystemId: meta.id,
      title: meta.title,
      figmaFileKey: fileKey,
      figmaUrl: meta.figmaFileUrl || meta.figmaUrl,
      entryNodeId: meta.figmaEntryNodeId,
      lastSyncedAt: now,
      lastSyncSource: "figma-rest-api",
      talkToFigmaChannel: meta.talkToFigmaChannel,
    },
    modes: flat.collections,
    tokens: flat.tokens,
    styles,
    _api: {
      variablesMetaKeys: Object.keys(metaBlock),
      fileName: fileJson.name,
      styleNodeCount: fileJson.styles ? Object.keys(fileJson.styles).length : 0,
      variableCount: flat.rawVariableCount,
    },
  };

  fs.mkdirSync(DS, { recursive: true });
  fs.writeFileSync(TOKENS_OUT, JSON.stringify(payload, null, 2) + "\n", "utf8");
  fs.writeFileSync(CSS_OUT, buildCssRoot(flat), "utf8");

  console.log(`Wrote ${path.relative(ROOT, TOKENS_OUT)}`);
  console.log(`Wrote ${path.relative(ROOT, CSS_OUT)}`);
  console.log(`Variables: ${flat.rawVariableCount}, style entries: ${payload._api.styleNodeCount}`);
}

main().catch(err => {
  console.error(err);
  process.exit(1);
});
